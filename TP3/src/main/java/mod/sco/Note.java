package mod.sco;

public class Note { 

		private int idnote; 
		private String numInscr; 
		private String matiere; 
		private float note;
		public int getIdnote() {
			return idnote;
		}
		public void setIdnote(int idnote) {
			this.idnote = idnote;
		}
		public String getNumInscr() {
			return numInscr;
		}
		public void setNumInscr(String numInscr) {
			this.numInscr = numInscr;
		}
		public String getMatiere() {
			return matiere;
		}
		public void setMatiere(String matiere) {
			this.matiere = matiere;
		}
		public float getNote() {
			return note;
		}
		public void setNote(float note) {
			this.note = note;
		}
		
		
		
}


